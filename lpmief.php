</div>
<div class="container pt-4">
  <h3>Organisation</h3>
  <hr>
  <p>La licence professionnelle se prépare en 1 an, à raison d'environ 600 heures d'enseignement, à l'université.</p>
  <p>La licence professionnelle est organisée en 2 semestres conférant chacun 30 crédits ECTS (European Credits Transfer System).</p>
  <p>La formation est composée d'UE (unités d'enseignement) regroupées par semestres. Les effectifs sont limités afin de garantir un bon encadrement.</p>
  <p>Les cours magistraux alternent avec des TD (travaux dirigés) auxquels s'ajoutent un projet tutoré collectif et une alternance en entreprise.</p>
  <p>Entre les cours et les TD, le projet tutoré et l'alternance en entreprise, l'année est intensive. Le rythme d'études est soutenu et la présence aux cours obligatoire. Beaucoup de travail personnel et de prise d'initiative sont attendus de la part de l'étudiant, notamment lors des périodes en entreprise.</p>
  <p>Pour l'apprenti/e, l'implication personnelle est double en cours et en emploi. Il faut savoir bien gérer son temps.</p>
</div>
<div class="container pt-4">
  <h3>Rythme d’alternance</h3>
  <hr>
  <p>2 mois continus à l'Institut et 2 mois continus en entreprise.</p>
</div>
